package programer.java.now.app;

/*
    Dalam source code dibawah ini merupakan sebuah code matematik yang 
    digunakan untuk menyelesaikan masalah numerik dan matematika. untuk
    menggunakan metematik dalam java menggunakan keyword math yang didalamnya
    terdapat berbagai method operasi matematik dan perlu di ingat math merupakan
    static keyword.
*/
public class MathApp {
    public static void main(String[] args) {
        
        int min = Math.min(2000, 1000);
        System.out.println(min);
        
        int max = Math.max(3000, 2000);
        System.out.println(max);
        
        System.out.println(Math.PI);
        
    }
    
}
